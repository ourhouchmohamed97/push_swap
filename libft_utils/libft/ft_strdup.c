#include "libft.h"

char	*ft_strdup(const char *s1)
{
	char	*tmp;
	int		length;
	int		i;

	i = 0;
	length = ft_strlen(s1);
	tmp = (char *)malloc(sizeof(char) * length + 1);
	if (!tmp)
		return (NULL);
	while (s1[i])
	{
		tmp[i] = s1[i];
		i++;
	}
	tmp[i] = '\0';
	return (tmp);
}
